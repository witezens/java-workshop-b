public class MinimumOfTwoProblem {
    public static void main(String[] args) {
        double a = 3;
        double b = 4;
        double m = 0;

        m = Math.min(a, b);

        String s = "The minimum of numbers: " + a + " and " +b + " is " + m;
        System.out.println(s);
    }
}
